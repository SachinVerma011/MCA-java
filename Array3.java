class Array3 
{
    public static void main(String[] args) 
    {
        int sum=0;
        int add =0;
        for (int i=0; i<=100; i++)
        {
          {
            if(i%5==0)
            {
            {
                continue ;
                
            }
            sum+=i;  
           }
           System.out.println("a"+sum);
          }
         
          {
            if(i%6==0)
            {
                continue ;
                
            }
            add+=i;
            System.out.println("b"+add);  
          }
        } 
        
    }

}  